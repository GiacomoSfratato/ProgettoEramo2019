package model;

public class Ristampa {
	
	private String data;
	private int numero;
	
	public Ristampa(String data, int numero) {
		this.data = data;
		this.numero = numero;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}
	
	
	

}
